export interface UserRole {
  id: string
  name: string
  description: string
  permissions: Permission[]
  isSystem: boolean
  createdBy: string
  createdDate: string
  lastModified: string
  userCount: number
}

export interface Permission {
  id: string
  name: string
  description: string
  category: string
  access: "read" | "write" | "admin" | "none"
}

export interface PortalUser {
  id: string
  username: string
  email: string
  firstName: string
  lastName: string
  role: string
  status: "active" | "inactive" | "pending" | "locked"
  lastLogin: string | null
  createdDate: string
  clinicId: string
  clinicName: string
  phoneNumber: string
  profileImage?: string
  mfaEnabled: boolean
  passwordLastChanged: string
  passwordExpiryDate: string
  loginAttempts: number
  notes?: string
}

export interface PortalActivity {
  id: string
  userId: string
  username: string
  action: string
  resource: string
  resourceId?: string
  timestamp: string
  ipAddress: string
  userAgent: string
  status: "success" | "failure"
  details?: string
}

